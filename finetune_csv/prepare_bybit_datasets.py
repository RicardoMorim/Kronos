from __future__ import annotations

import argparse
import json
import sys
from dataclasses import dataclass
from datetime import datetime, timedelta, timezone
from pathlib import Path
from typing import Iterable

REPO_ROOT = Path(__file__).resolve().parents[1]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from finetune_csv.bybit_data import fetch_bybit_klines


@dataclass(frozen=True)
class TimeframePreset:
    interval: str
    lookback_window: int
    predict_window: int
    max_context: int
    history_days: int


PRESETS: dict[str, TimeframePreset] = {
    "5": TimeframePreset(interval="5", lookback_window=512, predict_window=48, max_context=512, history_days=180),
    "60": TimeframePreset(interval="60", lookback_window=512, predict_window=48, max_context=512, history_days=730),
}


def parse_iso_to_utc_ms(value: str) -> int:
    dt = datetime.fromisoformat(value.replace("Z", "+00:00"))
    if dt.tzinfo is None:
        dt = dt.replace(tzinfo=timezone.utc)
    else:
        dt = dt.astimezone(timezone.utc)
    return int(dt.timestamp() * 1000)


def utc_now_ms() -> int:
    return int(datetime.now(tz=timezone.utc).timestamp() * 1000)


def compute_time_range_ms(
    start_iso: str | None,
    end_iso: str | None,
    history_days: int,
) -> tuple[int, int]:
    end_ms = parse_iso_to_utc_ms(end_iso) if end_iso else utc_now_ms()
    if start_iso:
        start_ms = parse_iso_to_utc_ms(start_iso)
    else:
        start_dt = datetime.fromtimestamp(end_ms / 1000, tz=timezone.utc) - timedelta(days=history_days)
        start_ms = int(start_dt.timestamp() * 1000)

    if start_ms >= end_ms:
        raise ValueError("start time must be earlier than end time")
    return start_ms, end_ms


def sanitize_symbol(symbol: str) -> str:
    return "".join(ch for ch in symbol.upper() if ch.isalnum() or ch in ("_", "-"))


def interval_label(interval: str) -> str:
    return f"{interval}m" if interval.isdigit() else interval.lower()


def build_output_csv_path(output_dir: Path, symbol: str, interval: str) -> Path:
    label = interval_label(interval)
    return output_dir / f"bybit_{sanitize_symbol(symbol)}_{label}.csv"


def build_config_path(config_dir: Path, symbol: str, interval: str) -> Path:
    label = interval_label(interval)
    return config_dir / f"config_bybit_{sanitize_symbol(symbol).lower()}_{label}.yaml"


