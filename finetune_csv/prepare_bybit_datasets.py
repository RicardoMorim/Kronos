def split_intervals(value: str) -> list[str]:
    intervals = [item.strip() for item in value.split(",") if item.strip()]
    if not intervals:
        raise ValueError("At least one interval is required")
    return intervals


def ensure_supported_or_default(interval: str) -> TimeframePreset:
    if interval in PRESETS:
        return PRESETS[interval]
    # sensible default for non-preset intervals
    return TimeframePreset(
        interval=interval,
        lookback_window=512,
        predict_window=48,
        max_context=512,
        history_days=365,
    )


def fetch_interval_dataset(
    category: str,
    symbol: str,
    interval: str,
    start_ms: int,
    end_ms: int,
    limit: int,
    max_pages: int,
) -> tuple[int, Path, int, int]:
    output_dir = Path("finetune_csv/data/bybit")
    output_dir.mkdir(parents=True, exist_ok=True)

    csv_path = build_output_csv_path(output_dir=output_dir, symbol=symbol, interval=interval)

    df = fetch_bybit_klines(
        category=category,
        symbol=symbol,
        interval=interval,
        start_ms=start_ms,
        end_ms=end_ms,
        limit=limit,
        max_pages=max_pages,
    )

    if df.empty:
        raise RuntimeError(f"No data returned for interval={interval} symbol={symbol}")

    df.to_csv(csv_path, index=False)
    got_start_ms = int(df["timestamps"].min().timestamp() * 1000)
    got_end_ms = int(df["timestamps"].max().timestamp() * 1000)
    return len(df), csv_path, got_start_ms, got_end_ms


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Fetch Bybit public candles and prepare CSV/config files for Kronos finetune_csv pipeline."
    )
    parser.add_argument("--symbol", default="BTCUSDT", help="Bybit symbol, e.g. BTCUSDT")
    parser.add_argument("--category", default="linear", choices=["linear", "inverse", "spot", "option"])
    parser.add_argument("--intervals", default="60,5", help="Comma-separated Bybit intervals, e.g. 60,5")
    parser.add_argument("--start-iso", default=None, help="Start datetime in ISO format, e.g. 2024-01-01T00:00:00Z")
    parser.add_argument("--end-iso", default=None, help="End datetime in ISO format, e.g. 2026-01-01T00:00:00Z")
    parser.add_argument("--history-days", type=int, default=None, help="History window in days if start-iso is omitted")
    parser.add_argument("--limit", type=int, default=1000, help="Rows per Bybit page")
    parser.add_argument("--max-pages", type=int, default=40, help="Max API pages per interval")
    parser.add_argument("--batch-size", type=int, default=32, help="Batch size for generated training configs")
    parser.add_argument(
        "--write-configs",
        action="store_true",
        help="Also generate finetune_csv config YAML files for each interval",
    )
    parser.add_argument(
        "--config-dir",
        default="finetune_csv/configs",
        help="Where to write generated config files when --write-configs is enabled",
    )
    parser.add_argument(
        "--summary-json",
        default="finetune_csv/data/bybit/prepare_summary.json",
        help="Where to write summary metadata",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    intervals = split_intervals(args.intervals)

    config_dir = Path(args.config_dir)
    output_root = Path("finetune_csv")

    summary: dict[str, object] = {
        "symbol": args.symbol,
        "category": args.category,
        "intervals": intervals,
        "created_at_utc": datetime.now(tz=timezone.utc).isoformat(),
        "datasets": [],
    }

    for interval in intervals:
        preset = ensure_supported_or_default(interval)
        history_days = args.history_days if args.history_days is not None else preset.history_days
        start_ms, end_ms = compute_time_range_ms(args.start_iso, args.end_iso, history_days=history_days)

        rows, csv_path, got_start_ms, got_end_ms = fetch_interval_dataset(
            category=args.category,
            symbol=args.symbol,
            interval=interval,
            start_ms=start_ms,
            end_ms=end_ms,
            limit=args.limit,
            max_pages=args.max_pages,
        )

        item: dict[str, object] = {
            "interval": interval,
            "rows": rows,
            "csv_path": csv_path.as_posix(),
            "requested_start_ms": start_ms,
            "requested_end_ms": end_ms,
            "actual_start_ms": got_start_ms,
            "actual_end_ms": got_end_ms,
            "history_days": history_days,
        }

        if args.write_configs:
            config_dir.mkdir(parents=True, exist_ok=True)
            cfg_path = build_config_path(config_dir=config_dir, symbol=args.symbol, interval=interval)
            cfg_text = build_training_config_yaml(
                data_path=csv_path,
                interval=interval,
                symbol=args.symbol,
                output_root=output_root,
                lookback_window=preset.lookback_window,
                predict_window=preset.predict_window,
                max_context=preset.max_context,
                batch_size=args.batch_size,
            )
            cfg_path.write_text(cfg_text, encoding="utf-8")
            item["config_path"] = cfg_path.as_posix()

        summary_datasets = summary["datasets"]
        if isinstance(summary_datasets, list):
            summary_datasets.append(item)

        print(
            f"[done] interval={interval} rows={rows} csv={csv_path.as_posix()}"
            + (f" config={item['config_path']}" if "config_path" in item else "")
        )

    summary_path = Path(args.summary_json)
    summary_path.parent.mkdir(parents=True, exist_ok=True)
    summary_path.write_text(json.dumps(summary, indent=2), encoding="utf-8")
    print(f"[summary] {summary_path.as_posix()}")


if __name__ == "__main__":
    main()
